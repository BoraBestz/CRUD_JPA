    //@PreAuthorize("hasAnyAuthorityCustom('ARMTE')")
    @PreAuthorize("isAuthenticated()")
    @PutMapping("/document/config/header")
    @ApiOperation(value = "Update DocumentConfigHeader", response = void.class)
    @ApiResponses(
            value = {
                    @ApiResponse(code = 400, message = "Something went wrong"),
                    @ApiResponse(code = 403, message = "Access denied"),
                    @ApiResponse(code = 500, message = "Error")
            })
    public ResponseEntity<ResponseModel> update(@RequestBody DocumentConfigHeaderDto dto) {
        /// FOR LOG///
        int status = 0;
        String remark = "";
        DocumentConfigHeaderDto dtoFromDb = null;
        DocumentConfigHeaderDto dtoFromDbCurrentData = null;
        /// FOR LOG///
        try {
            log.info("Start DocumentConfigHeaderController.update()... ");
            dtoFromDb = documentConfigHeaderService.getById(dto.getDocumentConfigHeaderId());
            dtoFromDb.setUpdateBy(oauthUser.oauthUserId());
            dtoFromDb.setUpdateDt(new Date());

            /// FOR LOG///
            /// prepare data
            dtoFromDbCurrentData = dtoFromDb.toBuilder().build(); // clone db obj
            /// FOR LOG///

            if (dtoFromDb.getDocumentConfigHeaderId() > 0) {
                if (dto.getDocumentConfigHeaderCode() != null
                        && !dtoFromDb.getDocumentConfigHeaderCode().equalsIgnoreCase(dto.getDocumentConfigHeaderCode())) {
                    int count = documentConfigHeaderService.checkDuplicateCode(dto);
                    if (count > 0) {
                        throw new BusinessServiceException(
                                HttpStatus.CONFLICT, message.getMessage("ERM0002"), "ERM0002");
                    }
                }

                // map data from front to db
                modelMapper.map(dto, dtoFromDb);
                dtoFromDb.setStatus("A");
                documentConfigHeaderService.save(dtoFromDb);

                return HelperUtils.responseSuccess();
            } else {
                throw new BusinessServiceException(
                        HttpStatus.NOT_FOUND, message.getMessage("ERM0001"), "ERM0001");
            }
        } catch (BusinessServiceException e) {
            log.error("Error DocumentConfigHeaderController.update()... " + e.getMessage());

            /// FOR LOG///
            status = e.getHttpStatus().value();
            remark = e.getMessage() + " ID=" + dto.getDocumentConfigHeaderId();
            /// FOR LOG///

            return HelperUtils.responseError(e.getHttpStatus(), e.getErrorCode(), e.getMessage());
        } catch (Exception e) {
            log.error(e.getMessage());

            /// FOR LOG///
            status = HttpStatus.INTERNAL_SERVER_ERROR.value();
            remark = e.getMessage() + " ID=" + dto.getDocumentConfigHeaderId();
            /// FOR LOG///

            return HelperUtils.responseErrorInternalServerError(
                    String.valueOf(HttpStatus.INTERNAL_SERVER_ERROR.value()), e.getMessage());
        } finally {

            // ***START ACTIVITY LOG***//
            loggingServiceClient.activityLog(
                    LoggingServiceClient.ActivityType.UPDATE,
                    dtoFromDbCurrentData,
                    dtoFromDb,
                    remark,
                    getApiOperationValue(new Object() {}),
                    getClientIp(),
                    getTracer(),
                    getMethodName(new Object() {}),
                    getToken(),
                    "XXX",
                    status);
            // ***END ACTIVITY LOG***//

            log.info("End DocumentConfigHeaderController.update()... ");
        }
    }

